-- 4. TRIGGER
-- TR_BORRA_AULA

CREATE OR REPLACE TRIGGER TR_BORRA_AULA
BEFORE DELETE ON AULA
FOR EACH ROW
DECLARE
    V_CONTADOR NUMBER;
BEGIN
    SELECT COUNT(*) INTO V_CONTADOR
    FROM EXAMEN
    WHERE AULA_CODIGO      = :OLD.CODIGO
      AND AULA_SEDE_CODIGO = :OLD.SEDE_CODIGO
      AND FECHAYHORA < SYSDATE;

    IF V_CONTADOR > 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'No se puede borrar: ya se han realizado examenes en esta aula');
    END IF;

    SELECT COUNT(*) INTO V_CONTADOR
    FROM EXAMEN
    WHERE AULA_CODIGO      = :OLD.CODIGO
      AND AULA_SEDE_CODIGO = :OLD.SEDE_CODIGO
      AND FECHAYHORA BETWEEN SYSDATE AND SYSDATE + 2;

    IF V_CONTADOR > 0 THEN
        RAISE_APPLICATION_ERROR(-20002, 'No se puede borrar: hay examenes en las proximas 48 horas');
    END IF;

    DELETE FROM VIGILA
    WHERE EXAMEN_AULA_CODIGO = :OLD.CODIGO
      AND EXAMEN_SEDE_CODIGO = :OLD.SEDE_CODIGO
      AND EXAMEN_FECHAYHORA  > SYSDATE;

    DELETE FROM ASISTENCIA
    WHERE EXAMEN_AULA_CODIGO = :OLD.CODIGO
      AND EXAMEN_SEDE_CODIGO = :OLD.SEDE_CODIGO
      AND EXAMEN_FECHAYHORA  > SYSDATE;

    DELETE FROM EXAMEN_ES_MATERIA
    WHERE EXAMEN_AULA_CODIGO = :OLD.CODIGO
      AND EXAMEN_SEDE_CODIGO = :OLD.SEDE_CODIGO
      AND EXAMEN_FECHAYHORA  > SYSDATE;

    DELETE FROM EXAMEN
    WHERE AULA_CODIGO      = :OLD.CODIGO
      AND AULA_SEDE_CODIGO = :OLD.SEDE_CODIGO
      AND FECHAYHORA       > SYSDATE;

END TR_BORRA_AULA;
