-- V_OCUPACION_ASIGNADA

CREATE OR REPLACE VIEW V_OCUPACION_ASIGNADA AS
SELECT 
    s.CODIGO                 AS SEDE_CODIGO,
    s.NOMBRE                 AS SEDE_NOMBRE,
    a.EXAMEN_AULA_CODIGO     AS AULA_CODIGO,
    a.EXAMEN_FECHAYHORA      AS FECHA_EXAMEN,
    COUNT(a.ESTUDIANTE_DNI)  AS NUM_ESTUDIANTES
FROM SEDE s
JOIN ASISTENCIA a ON a.EXAMEN_SEDE_CODIGO = s.CODIGO
GROUP BY 
    s.CODIGO,
    s.NOMBRE,
    a.EXAMEN_AULA_CODIGO,
    a.EXAMEN_FECHAYHORA;

-- V_OCUPACION

CREATE OR REPLACE VIEW V_OCUPACION AS
SELECT 
    s.CODIGO                 AS SEDE_CODIGO,
    s.NOMBRE                 AS SEDE_NOMBRE,
    a.EXAMEN_AULA_CODIGO     AS AULA_CODIGO,
    a.EXAMEN_FECHAYHORA      AS FECHA_EXAMEN,
    COUNT(a.ESTUDIANTE_DNI)  AS NUM_ESTUDIANTES
FROM SEDE s
JOIN ASISTENCIA a ON a.EXAMEN_SEDE_CODIGO = s.CODIGO
WHERE a.ASISTE = 'S'
GROUP BY 
    s.CODIGO,
    s.NOMBRE,
    a.EXAMEN_AULA_CODIGO,
    a.EXAMEN_FECHAYHORA;

-- V_VIGILANTES

CREATE OR REPLACE VIEW V_VIGILANTES AS
SELECT 
    s.CODIGO                 AS SEDE_CODIGO,
    s.NOMBRE                 AS SEDE_NOMBRE,
    v.EXAMEN_AULA_CODIGO     AS AULA_CODIGO,
    v.EXAMEN_FECHAYHORA      AS FECHA_EXAMEN,
    COUNT(v.VOCAL_DNI)       AS NUM_VIGILANTES
FROM SEDE s
JOIN VIGILA v ON v.EXAMEN_SEDE_CODIGO = s.CODIGO
GROUP BY 
    s.CODIGO,
    s.NOMBRE,
    v.EXAMEN_AULA_CODIGO,
    v.EXAMEN_FECHAYHORA;

-- 2 PAQUETE PK_OCUPACION
-- OCUPACION_MAXIMA

-- CABECERA
CREATE OR REPLACE PACKAGE PK_OCUPACION AS
    FUNCTION OCUPACION_MAXIMA(P_SEDE NUMBER, P_AULA NUMBER) RETURN NUMBER;
    FUNCTION OCUPACION_OK RETURN BOOLEAN;
    FUNCTION VOCAL_DUPLICADO(P_VOCAL_DNI VARCHAR2) RETURN BOOLEAN;
    FUNCTION VOCALES_DUPLICADOS RETURN BOOLEAN;
    FUNCTION VOCAL_RATIO(P_RATIO NUMBER) RETURN BOOLEAN;
END PK_OCUPACION;
/

-- CUERPO
CREATE OR REPLACE PACKAGE BODY PK_OCUPACION AS

    FUNCTION OCUPACION_MAXIMA(P_SEDE NUMBER, P_AULA NUMBER) RETURN NUMBER AS
        V_RESULTADO NUMBER;
    BEGIN
        SELECT MAX(ALUMNOS + VOCALES)
        INTO V_RESULTADO
        FROM (
            SELECT
                COUNT(a.ESTUDIANTE_DNI) AS ALUMNOS,
                COUNT(v.VOCAL_DNI)      AS VOCALES
            FROM ASISTENCIA a
            LEFT JOIN VIGILA v
                ON  v.EXAMEN_FECHAYHORA  = a.EXAMEN_FECHAYHORA
                AND v.EXAMEN_AULA_CODIGO = a.EXAMEN_AULA_CODIGO
                AND v.EXAMEN_SEDE_CODIGO = a.EXAMEN_SEDE_CODIGO
            WHERE a.EXAMEN_SEDE_CODIGO = P_SEDE
              AND a.EXAMEN_AULA_CODIGO = P_AULA
              AND a.ASISTE = 'S'
            GROUP BY a.EXAMEN_FECHAYHORA
        );
        RETURN NVL(V_RESULTADO, 0);
    END OCUPACION_MAXIMA;

    FUNCTION OCUPACION_OK RETURN BOOLEAN AS
        V_CONTADOR NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_CONTADOR
        FROM EXAMEN e
        JOIN AULA au ON au.CODIGO = e.AULA_CODIGO 
                    AND au.SEDE_CODIGO = e.AULA_SEDE_CODIGO
        WHERE e.FECHAYHORA > SYSDATE
          AND (
              (SELECT COUNT(*) FROM ASISTENCIA a
               WHERE a.EXAMEN_FECHAYHORA  = e.FECHAYHORA
                 AND a.EXAMEN_AULA_CODIGO = e.AULA_CODIGO
                 AND a.EXAMEN_SEDE_CODIGO = e.AULA_SEDE_CODIGO
              ) > au.CAPACIDAD_EXAMEN
              OR
              (SELECT COUNT(*) FROM ASISTENCIA a
               WHERE a.EXAMEN_FECHAYHORA  = e.FECHAYHORA
                 AND a.EXAMEN_AULA_CODIGO = e.AULA_CODIGO
                 AND a.EXAMEN_SEDE_CODIGO = e.AULA_SEDE_CODIGO
              ) +
              (SELECT COUNT(*) FROM VIGILA v
               WHERE v.EXAMEN_FECHAYHORA  = e.FECHAYHORA
                 AND v.EXAMEN_AULA_CODIGO = e.AULA_CODIGO
                 AND v.EXAMEN_SEDE_CODIGO = e.AULA_SEDE_CODIGO
              ) > au.CAPACIDAD
          );
        RETURN V_CONTADOR = 0;
    END OCUPACION_OK;

    FUNCTION VOCAL_DUPLICADO(P_VOCAL_DNI VARCHAR2) RETURN BOOLEAN AS
        V_CONTADOR NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_CONTADOR
        FROM (
            SELECT EXAMEN_FECHAYHORA
            FROM VIGILA
            WHERE VOCAL_DNI = P_VOCAL_DNI
            GROUP BY EXAMEN_FECHAYHORA
            HAVING COUNT(*) > 1
        );
        RETURN V_CONTADOR > 0;
    END VOCAL_DUPLICADO;

    FUNCTION VOCALES_DUPLICADOS RETURN BOOLEAN AS
    BEGIN
        FOR v_vocal IN (SELECT DNI FROM VOCAL) LOOP
            IF VOCAL_DUPLICADO(v_vocal.DNI) THEN
                RETURN TRUE;
            END IF;
        END LOOP;
        RETURN FALSE;
    END VOCALES_DUPLICADOS;

    FUNCTION VOCAL_RATIO(P_RATIO NUMBER) RETURN BOOLEAN AS
        V_CONTADOR NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_CONTADOR
        FROM EXAMEN e
        WHERE e.FECHAYHORA > SYSDATE
          AND (
              (SELECT COUNT(*) FROM ASISTENCIA a
               WHERE a.EXAMEN_FECHAYHORA  = e.FECHAYHORA
                 AND a.EXAMEN_AULA_CODIGO = e.AULA_CODIGO
                 AND a.EXAMEN_SEDE_CODIGO = e.AULA_SEDE_CODIGO
              )
              >
              (SELECT COUNT(*) FROM VIGILA v
               WHERE v.EXAMEN_FECHAYHORA  = e.FECHAYHORA
                 AND v.EXAMEN_AULA_CODIGO = e.AULA_CODIGO
                 AND v.EXAMEN_SEDE_CODIGO = e.AULA_SEDE_CODIGO
              ) * P_RATIO
          );
        RETURN V_CONTADOR = 0;
    END VOCAL_RATIO;

END PK_OCUPACION;
