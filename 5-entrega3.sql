CREATE OR REPLACE PACKAGE BODY PK_SEGURIDAD_PAU AS

    PROCEDURE PR_CREA_ESTUDIANTE(P_DNI IN VARCHAR2, P_USUARIO OUT VARCHAR2, P_PASSWORD OUT VARCHAR2) AS
    BEGIN
        P_USUARIO  := 'EST_' || P_DNI;
        P_PASSWORD := DBMS_RANDOM.STRING('A', 10) || TRUNC(DBMS_RANDOM.VALUE(10,99));

        EXECUTE IMMEDIATE 'CREATE USER ' || P_USUARIO || ' IDENTIFIED BY "' || P_PASSWORD || '"';
        EXECUTE IMMEDIATE 'GRANT CREATE SESSION TO ' || P_USUARIO;
        EXECUTE IMMEDIATE 'GRANT SELECT ON PAU.ASISTENCIA TO ' || P_USUARIO;
        EXECUTE IMMEDIATE 'GRANT SELECT ON PAU.EXAMEN TO ' || P_USUARIO;
    END PR_CREA_ESTUDIANTE;

    PROCEDURE PR_CREA_VOCAL(P_DNI IN VARCHAR2, P_USUARIO OUT VARCHAR2, P_PASSWORD OUT VARCHAR2) AS
    BEGIN
        P_USUARIO  := 'VOC_' || P_DNI;
        P_PASSWORD := DBMS_RANDOM.STRING('A', 10) || TRUNC(DBMS_RANDOM.VALUE(10,99));

        EXECUTE IMMEDIATE 'CREATE USER ' || P_USUARIO || ' IDENTIFIED BY "' || P_PASSWORD || '"';
        EXECUTE IMMEDIATE 'GRANT CREATE SESSION TO ' || P_USUARIO;
        EXECUTE IMMEDIATE 'GRANT SELECT ON PAU.EXAMEN TO ' || P_USUARIO;
        EXECUTE IMMEDIATE 'GRANT SELECT ON PAU.AULA TO ' || P_USUARIO;
        EXECUTE IMMEDIATE 'GRANT SELECT ON PAU.SEDE TO ' || P_USUARIO;
        EXECUTE IMMEDIATE 'GRANT SELECT ON PAU.VIGILA TO ' || P_USUARIO;
    END PR_CREA_VOCAL;

END PK_SEGURIDAD_PAU;
